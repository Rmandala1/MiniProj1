	<p>O primeiro benefício é remover aquela mudança de página chocante quando você clica em um link. 
		Em um SPA, os controles de navegação e a interface principal geralmente permanecem na página quando você clica em um link; apenas a parte do conteúdo que você deseja alterar realmente é alterada. 
		O segundo benefício é o aumento básico de velocidade que você obtém de uma carga útil de resposta do servidor mais leve para o bloco de conteúdo em comparação com uma página inteira. 
		A carga útil mais leve é transmitida pela rede mais rapidamente e o navegador pode incorporar o novo conteúdo mais rapidamente do que redesenhar uma página totalmente nova. 
